package main

import "fmt"

func main() {
	// The Println function comes from the "fmt" (format) package.
	// It prints the text and then moves to a new line automatically.
	fmt.Println("Hello, Go Developer!")

	// You can print multiple things at once by separating them with commas.
	// Go will automatically add a space between them.
	fmt.Println("I am", 22, "years old and I love", true, "adventures.")
}
